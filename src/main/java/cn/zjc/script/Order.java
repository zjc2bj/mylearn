package cn.zjc.script;

public class Order {

	private Integer busiStatus;
	private Integer orderFlow;
	private String remark;
	public Integer getBusiStatus() {
		return busiStatus;
	}

	public void setBusiStatus(Integer busiStatus) {
		this.busiStatus = busiStatus;
	}

	public Integer getOrderFlow() {
		return orderFlow;
	}

	public void setOrderFlow(Integer orderFlow) {
		this.orderFlow = orderFlow;
	}

	public String getRemark() {
		return remark;
	}

	public void setRemark(String remark) {
		this.remark = remark;
	}
}
